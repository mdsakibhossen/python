# Python Operators

# Arithmetic Operators:
print("Arithmetic Operators:")

# Addition
print("Addition")
print("1 + 2 =", 1 + 2)
print("10 + 5 =", 10 + 5)
print()  # Blank line for spacing

# Subtraction
print("Subtraction")
print("4 - 3 =", 4 - 3)
print("15 - 8 =", 15 - 8)
print()  # Blank line for spacing

# Multiplication
print("Multiplication")
print("4 * 3 =", 4 * 3)
print("6 * 7 =", 6 * 7)
print()  # Blank line for spacing

# Division
print("Division")
print("4 / 3 =", 4 / 3)
print("16 / 4 =", 16 / 4)
print()  # Blank line for spacing

# Modulus (remainder)
print("Modulus (remainder)")
print("4 % 3 =", 4 % 3)
print("15 % 6 =", 15 % 6)
print()  # Blank line for spacing

# Floor Division (division without the remainder)
print("Floor Division")
print("4 // 3 =", 4 // 3)
print("16 // 5 =", 16 // 5)
print()  # Blank line for spacing

# Exponentiation (power)
print("Exponentiation (power)")
print("4 ** 3 =", 4 ** 3)
print("2 ** 5 =", 2 ** 5)
print()  # Blank line for spacing
