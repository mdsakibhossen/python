# Assignment Operators
print("Assignment Operators")

# Assign
print("Assign (=)")
x = 10
print("x =", x)  # Assigning value to x
print()  # Blank line for spacing

# Add and Assign (+=)
print("Add and Assign (+=)")
x += 5
print("x after x += 5:", x)  # x is now 15
print()  # Blank line for spacing

# Subtract and Assign (-=)
print("Subtract and Assign (-=)")
x -= 3
print("x after x -= 3:", x)  # x is now 12
print()  # Blank line for spacing

# Multiply and Assign (*=)
print("Multiply and Assign (*=)")
x *= 2
print("x after x *= 2:", x)  # x is now 24
print()  # Blank line for spacing

# Divide and Assign (/=)
print("Divide and Assign (/=)")
x /= 4
print("x after x /= 4:", x)  # x is now 6.0
print()  # Blank line for spacing

# Modulus and Assign (%=)
print("Modulus and Assign (%=)")
x %= 5
print("x after x %= 5:", x)  # x is now 1.0
print()  # Blank line for spacing

# Exponentiation and Assign (**=)
print("Exponentiation and Assign (**=)")
x **= 3
print("x after x **= 3:", x)  # x is now 1.0
print()  # Blank line for spacing

# Floor Division and Assign (//=)
print("Floor Division and Assign (//=)")
x //= 1
print("x after x //= 1:", x)  # x remains 1.0
print()  # Blank line for spacing
