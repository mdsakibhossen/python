# Comparison Operators
print("Comparison Operators")

# Equal (==)
print("Equal (==)")
print("5 == 5:", 5 == 5)        # True, because both are equal
print("5 == 3:", 5 == 3)        # False, because they are not equal
print()  # Blank line for spacing

# Not Equal (!=)
print("Not Equal (!=)")
print("5 != 5:", 5 != 5)        # False, because both are equal
print("5 != 3:", 5 != 3)        # True, because they are not equal
print()  # Blank line for spacing

# Greater Than (>)
print("Greater Than (>)")
print("5 > 3:", 5 > 3)          # True, because 5 is greater than 3
print("3 > 5:", 3 > 5)          # False, because 3 is not greater than 5
print()  # Blank line for spacing

# Less Than (<)
print("Less Than (<)")
print("3 < 5:", 3 < 5)          # True, because 3 is less than 5
print("5 < 3:", 5 < 3)          # False, because 5 is not less than 3
print()  # Blank line for spacing

# Greater Than or Equal To (>=)
print("Greater Than or Equal To (>=)")
print("5 >= 5:", 5 >= 5)        # True, because both are equal
print("5 >= 3:", 5 >= 3)        # True, because 5 is greater than 3
print()  # Blank line for spacing

# Less Than or Equal To (<=)
print("Less Than or Equal To (<=)")
print("3 <= 5:", 3 <= 5)        # True, because 3 is less than 5
print("5 <= 5:", 5 <= 5)        # True, because both are equal
# False, because 5 is not less than or equal to 3
print("5 <= 3:", 5 <= 3)
print()  # Blank line for spacing
