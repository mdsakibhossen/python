# Membership Operators
print("Membership Operators")

# In (True if the value is found in the sequence)
# Example with a list
my_list = [1, 2, 3, 4, 5]
print("In (True if the value is found in the sequence)")
print("2 in my_list:", 2 in my_list)            # True
print("6 in my_list:", 6 in my_list)            # False
print()  # Blank line for spacing

# Example with a string
my_string = "Hello, World!"
print("Checking membership in a string")
print("'H' in my_string:", 'H' in my_string)    # True
print("'h' in my_string:", 'h' in my_string)    # False (case-sensitive)
print()  # Blank line for spacing

# Not in (True if the value is not found in the sequence)
print("Not In (True if the value is not found in the sequence)")
print("10 not in my_list:", 10 not in my_list)  # True
print("3 not in my_list:", 3 not in my_list)    # False
print()  # Blank line for spacing
